async function main() {
	console.log("Start seeding...");
	for (const b of bottles) {
		const existing = await prisma.bottle.findFirst({
			where: { name: b.name },
		});

		let bottle;
		if (existing) {
			bottle = await prisma.bottle.update({
				where: { id: existing.id },
				data: b,
			});
			console.log(`Updated bottle with id: ${bottle.id}`);
		} else {
			bottle = await prisma.bottle.create({
				data: b,
			});
			console.log(`Created bottle with id: ${bottle.id}`);
		}
	}

	console.log("Seeding cocktails...");
	for (const c of cocktails) {
		const existing = await prisma.cocktail.findFirst({
			where: { name: c.name },
		});

		let cocktail;
		if (existing) {
			// Update existing cocktail
			cocktail = await prisma.cocktail.update({
				where: { id: existing.id },
				data: {
					description: c.description,
					method: c.method,
					tasteNote: c.tasteNote,
					// Re-create ingredients
					ingredients: {
						deleteMany: {},
						create: c.ingredients.map((name) => ({
							name,
							quantity: null, // As per markdown data, quantity isn't specified
						})),
					},
				},
			});
			console.log(`Updated cocktail with id: ${cocktail.id}`);
		} else {
			// Create new cocktail
			cocktail = await prisma.cocktail.create({
				data: {
					name: c.name,
					description: c.description,
					method: c.method,
					tasteNote: c.tasteNote,
					ingredients: {
						create: c.ingredients.map((name) => ({
							name,
							quantity: null,
						})),
					},
				},
			});
			console.log(`Created cocktail with id: ${cocktail.id}`);
		}
	}
	console.log("Seeding finished.");
}

main()
	.catch((e) => {
		console.error(e);
		process.exit(1);
	})
	.finally(async () => {
		await prisma.$disconnect();
	});
